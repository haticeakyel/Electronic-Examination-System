package exam.pojo;

public class ExaminationSystem {

	private String systemCourseCode;
	private String systemCourseName;
	private String systemExamType;
	private String systemExamDate;
	private String systemExamTime;
	
	public String getSystemCourseCode() {
		return systemCourseCode;
	}
	
	public void setSystemCourseCode(String systemCourseCode) {
		this.systemCourseCode=systemCourseCode;
	}
	
	public String getSystemCourseName() {
		return systemCourseName;
	}
	
	public void setSystemCourseName(String systemCourseName) {
		this.systemCourseName=systemCourseName;
	}
	
	public String getSystemExamType() {
		return systemExamType;
	}
	
	public void setSystemExamType(String systemExamType) {
		this.systemExamType=systemExamType;
	}
	
	public String getSystemExamDate() {
		return systemExamDate;
	}
	
	public void setSystemExamDate(String systemExamDate) {
		this.systemExamDate=systemExamDate;
	}
	
	public String getSystemExamTime() {
		return systemExamTime;
	}
	
	public void setSystemExamTime(String systemExamTime) {
		this.systemExamTime=systemExamTime;
	}
	
	


}
